#include <math.h>
//回転行列
int KaitenGyouretu( a, int b, int c){
    cin >> a >> b >> d;
    double rad = M_PI * d / 180;
    cout << setprecision(24) << (long double)cos(rad) * a + (long double)-sin(rad) * b << ' '
    << (long double)sin(rad) * a + (long double)cos(rad) * b << endl;
}
